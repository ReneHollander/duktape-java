package org.duktape.duktapejava.generator;

public class Define {

    public String name;
    public String value;
    public String comment;

    @Override
    public String toString() {
        return "Define{" +
                "name='" + name + '\'' +
                ", value=" + value +
                ", comment='" + comment + '\'' +
                '}';
    }
}
